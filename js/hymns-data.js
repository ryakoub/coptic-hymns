window.formatGroupLabel = function (group) {
  if (!group) return "";
  if (group === "college") return "COLLEGE & UP";
  if (group === "kg") return "KG";
  if (group === "ms") return "MIDDLE SCHOOL";
  if (group === "hs") return "HIGH SCHOOL";
  return group.replace(/-/g, " ").replace(/\b\w/g, function (c) { return c.toUpperCase(); });
};

window.formatLevelLabel = function (level) {
  if (!level) return "";
  return level.replace("level-", "Level ");
};
